export function salvarLocalStorage(chave, informacao) {
  localStorage.setItem(chave, JSON.stringify(informacao));
}

export function lerLocalStorage(chave) {
  return JSON.parse(localStorage.getItem(chave));
}

export function mudarFoto() {
  for (const produtosCatalogo of catalogo) {
    if (lerLocalStorage("imovel") === produtosCatalogo.id) {
      const imagemPrincipal = document.getElementById(
        `imagem-principal-${produtosCatalogo.id}`
      );
      const imagemPrincipal1 = document.getElementById(
        `imagem-principal-1-${produtosCatalogo.id}`
      );
      const imagemSecundaria = document.getElementById(
        `imagem-secundaria-${produtosCatalogo.id}`
      );
      const imagemTerciaria = document.getElementById(
        `imagem-terciaria-${produtosCatalogo.id}`
      );
      const imageQuartenaria = document.getElementById(
        `imagem-quartenaria-${produtosCatalogo.id}`
      );

      imagemPrincipal1.addEventListener("click", () => {
        imagemPrincipal.src = imagemPrincipal1.src;
      });

      imagemSecundaria.addEventListener("click", () => {
        imagemPrincipal.src = imagemSecundaria.src;
      });

      imagemTerciaria.addEventListener("click", () => {
        imagemPrincipal.src = imagemTerciaria.src;
      });

      imageQuartenaria.addEventListener("click", () => {
        imagemPrincipal.src = imageQuartenaria.src;
      });
    }
  }
}

export function chamarNoWhats() {
  const botaoWhats = document.getElementById("botao-whats");
  const botaoWhatsVerMais = document.getElementById("botao-whats-ver-mais");

  botaoWhats.addEventListener("click", () => {
    const mensagem = encodeURIComponent(
      "Olá, gostaira de ser atendido por um especialista."
    );
    const fone = "5551993629999";

    window.open(`https://wa.me/${fone}?text=${mensagem}`, "_blank");
  });

  botaoWhatsVerMais.addEventListener("click", () => {
    for (const imovelCatalogo of catalogo) {
      if (lerLocalStorage("imovel") === imovelCatalogo.id) {
        const mensagem = encodeURIComponent(
          `Olá, gostaira de mais informações sobre o imóvel - ${imovelCatalogo.nome}.`
        );

        const fone = "5551993629999";

        window.open(`https://wa.me/${fone}?text=${mensagem}`, "_blank");
      }
    }
  });
}

export function irParaFavoritos() {
  const botaoFavoritos = document.getElementById("botao-favoritos");
  const containerProdutos = document.getElementById("container-produtos");
  const containerProdutosVerMais = document.getElementById(
    "container-produto-ver-mais"
  );
  botaoFavoritos.addEventListener("click", () => {
    if (
      !lerLocalStorage("idfavorito") ||
      lerLocalStorage("idfavorito") === "[]"
    ) {
      alert("Você não tem nenhum imóvel adicionado aos favoritos.");
      return;
    } else {
      window.location.href = "../favoritos.html";
    }
  });
}
